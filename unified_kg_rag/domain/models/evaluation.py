from datetime import datetime
from enum import Enum
from typing import Any

from pydantic import BaseModel, Field


class EvaluationMetricType(str, Enum):
    ANSWER_CORRECTNESS = "answer_correctness"
    ANSWER_RELEVANCY = "answer_relevancy"
    CONTEXT_PRECISION = "context_precision"
    CONTEXT_RECALL = "context_recall"
    CORRECTNESS = "correctness"
    FAITHFULNESS = "faithfulness"
    PARTIAL_CORRECTNESS = "partial_correctness"
    # Graph-aware retrieval metrics (use EvaluationGroundTruth.expected_*).
    # Coverage = fraction of expected graph artifacts surfaced in the answer
    # (i.e. recall). We deliberately do NOT emit precision/F1: precision would
    # require enumerating the answer's own entities/relationships, which we
    # cannot do from free text without a separate extractor — emitting it as a
    # copy of recall (the previous behaviour) overstated the signal.
    ENTITY_COVERAGE = "entity_coverage"
    RELATIONSHIP_COVERAGE = "relationship_coverage"


class EvaluatorType(str, Enum):
    LANGCHAIN = "langchain"
    RAGAS = "ragas"
    GRAPH_AWARE = "graph_aware"


class EvaluationQuery(BaseModel):
    query_id: str = Field(description="Unique identifier for the evaluation query.")
    question: str = Field(description="The question text to be evaluated.")
    category: str | None = Field(
        default=None, description="Optional category classification for the query."
    )
    difficulty: str | None = Field(
        default=None, description="Optional difficulty level indicator for the query."
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional metadata and custom attributes for the query.",
    )


class EvaluationGroundTruth(BaseModel):
    query_id: str = Field(description="Unique identifier of the associated query.")
    ground_truth: str = Field(description="The expected correct answer text.")
    reference_sources: list[str] = Field(
        default_factory=list,
        description="List of authoritative source references for the ground truth.",
    )
    expected_entities: list[str] = Field(
        default_factory=list,
        description="List of entities that should be present in the answer.",
    )
    expected_relationships: list[str] = Field(
        default_factory=list,
        description="List of relationships that should be identified in the answer.",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional metadata and custom attributes for the ground truth.",
    )


class EvaluationResult(BaseModel):
    query_id: str = Field(description="Unique identifier of the evaluated query.")
    question: str = Field(description="The original question that was evaluated.")
    generated_answer: str = Field(description="The answer generated by the system.")
    ground_truth: str = Field(description="The expected correct answer for comparison.")
    retrieved_contexts: list[str] = Field(
        default_factory=list,
        description="List of context passages retrieved during answer generation.",
    )
    response_time: float | None = Field(
        default=None, description="Time taken to generate the answer in seconds."
    )
    enable_thinking: bool = Field(
        default=False,
        description="Enable thinking mode for language model reasoning and step-by-step problem solving",
    )
    search_strategy: str | None = Field(
        default=None,
        description="Search strategy used for this evaluation. Options: auto, drift, local, global, simple",
    )
    search_type: str | None = Field(
        default=None,
        description="Search type used for this evaluation. Options: hybrid, lexical, vector",
    )
    top_k: int | None = Field(
        default=None, description="Number of top results retrieved for this evaluation."
    )
    retrieval_multiplier: int | None = Field(
        default=None, description="Retrieval multiplier used for this evaluation."
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional metadata and system-specific information.",
    )


class EvaluationMetric(BaseModel):
    metric_type: EvaluationMetricType = Field(
        description="The type of evaluation metric."
    )
    value: float = Field(description="The numerical value of the metric score.")
    explanation: str | None = Field(
        default=None,
        description="Optional explanation or reasoning for the metric score.",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional metadata specific to this metric calculation.",
    )


class EvaluationReport(BaseModel):
    query_id: str = Field(description="Unique identifier of the evaluated query.")
    evaluator_type: EvaluatorType = Field(
        description="The evaluator that generated this report."
    )
    metrics: list[EvaluationMetric] = Field(
        description="List of calculated evaluation metrics for this query."
    )
    overall_score: float | None = Field(
        default=None, description="Aggregated overall score across all metrics."
    )
    evaluation_time: datetime = Field(
        default_factory=datetime.now,
        description="Timestamp when the evaluation was performed.",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional metadata about the evaluation process.",
    )


class EvaluationSummary(BaseModel):
    total_queries: int = Field(
        description="Total number of queries in the evaluation dataset."
    )
    successful_evaluations: int = Field(
        description="Number of queries successfully evaluated."
    )
    failed_evaluations: int = Field(
        description="Number of queries that failed during evaluation."
    )
    average_response_time: float | None = Field(
        default=None,
        description="Average response time across all successful evaluations in seconds.",
    )
    metric_statistics: dict[str, dict[str, float]] = Field(
        default_factory=dict,
        description="Statistical summary for each metric type (mean, median, std, min, max, count).",
    )
    evaluation_start_time: datetime = Field(
        description="Timestamp when the evaluation run started."
    )
    evaluation_end_time: datetime = Field(
        description="Timestamp when the evaluation run completed."
    )
    configuration: dict[str, Any] = Field(
        default_factory=dict,
        description="The evaluation configuration settings used for this run.",
    )
